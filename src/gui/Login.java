    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if (fieldEmail.getText().isEmpty() || fieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Os campos não podem estar vazios!");
        } else if (!fieldEmail.getText().endsWith("@uepa.br")) {
            JOptionPane.showMessageDialog(rootPane, "Você deve utilizar um e-mail institucional!");
        } else {
            String email = fieldEmail.getText();
            String senha = fieldSenha.getText();

            LoginDAO dao = new LoginDAO();
            try {
                if (dao.fazerLogin(email, senha)) {
                    JOptionPane.showMessageDialog(rootPane, "Login feito com sucesso!");

                    Navegacao telaNav = new Navegacao();
                    telaNav.setVisible(true);

                    this.dispose();
                }
            } catch (Exception ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro no login!" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

